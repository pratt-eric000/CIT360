package com.prt.models;

import java.io.Serializable;

/**
 *
 * @author P-ratt
 */
public class UserRoleXref implements Serializable {

	private int id;
	private int userId;
	private int roleId;

	public UserRoleXref() {

	}

	public UserRoleXref(int userId, int roleId) {
		this.userId = userId;
		this.roleId = roleId;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public int getRoleId() {
		return roleId;
	}

	public void setRoleId(int roleId) {
		this.roleId = roleId;
	}
}
